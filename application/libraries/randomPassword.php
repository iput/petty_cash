class RandomPassword{
    public function acakpass($panjang)
{
    $karakter= 'NINDYagustina123456789';
    $string = '';
    for ($i = 0; $i < $panjang; $i++) {
    $pos = rand(0, strlen($karakter)-1);
    $string .= $karakter{$pos};
    }
    return $string;
}
}
